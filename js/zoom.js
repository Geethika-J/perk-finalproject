function zoomin1() 
{ 
    var img = document.getElementById('img1');
    var height = img.clientHeight;
    img.style.height = (height + 40) + "px";
    var width = img.clientWidth; 
    img.style.width = (width + 100) + "px"; 
} 
function zoomout1() 
{
    var img = document.getElementById('img1');
    var height = img.clientHeight;
    img.style.height = (height - 40) + "px";
    var width = img.clientWidth; 
    img.style.width = (width - 100) + "px"; 
}
